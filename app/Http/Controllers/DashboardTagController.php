    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Tag  $tag
     * @return \Illuminate\Http\Response
     */
    public function destroy(Tag $tag)
    {
        try {
            Tag::destroy($tag->id);
        } catch (Exception $th) {
            return redirect('/admin/dashboard/tags')->with('error', "Tags cannot be deleted!");
        }

        return redirect('/admin/dashboard/tags')->with('success', "Tags has been deleted!");
    }
